    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()
            || jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null || jTextField5.getText().isEmpty()
            || jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campos vazios, por favor preencher todos os campos!");
        }else{
            Bebida bebida = new Bebida();
            bebida.setCod(Integer.parseInt(jTextField1.getText()));
            bebida.setCod_de_barras(jTextField2.getText());
            bebida.setDescricao(jTextField3.getText());
            bebida.setMarca(jTextField4.getText());
            bebida.setGp_mercadoria(jComboBox1.getSelectedItem().toString());
            bebida.setT_do_item(jComboBox2.getSelectedItem().toString());
            bebida.setQ_estoque(Integer.parseInt(jTextField5.getText()));
            bebida.setV_unitario(Double.parseDouble(jTextField6.getText()));

            String valorUnitarioStr = jTextField6.getText().replace("R$", "").trim().replace(",", ".");
            bebida.setV_unitario(Double.parseDouble(valorUnitarioStr));

            try{
                BebidaDAO dao = new BebidaDAO();
                dao.AlterarBebida(bebida);
                JOptionPane.showMessageDialog(null, "Bebida atualizada com sucesso!");
                Tela_ajuste_bebida tab = new Tela_ajuste_bebida();
                tab.setVisible(true);
                dispose();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Nao foi possivel atualizar a bebida: " +e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void PreencherCampos(){
        jTextField1.setText(String.valueOf(bebida.getCod()));
        jTextField2.setText(bebida.getCod_de_barras());
        jTextField3.setText(bebida.getDescricao());
        jTextField4.setText(bebida.getMarca());
        jComboBox1.setSelectedItem(bebida.getGp_mercadoria());
        jComboBox2.setSelectedItem(bebida.getT_do_item());
        jTextField5.setText(String.valueOf(bebida.getQ_estoque()));
        jTextField6.setText(String.valueOf(bebida.getV_unitario()));
    }
